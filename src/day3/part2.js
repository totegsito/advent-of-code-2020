const parseInput = input => {
    const lines = input.split('\n')
    const numberOfLines = lines.length;
    const lineWidth = lines[0].length;
    return { lineWidth, numberOfLines, text: input, lines };
}

const move = (input, right, down) => {
    const { lines, lineWidth } = input
    let treeCounter = 0;
    let index = 0;
    for(let i = 0; i < lines.length; i += down) {
        const pos = (index * right) % lineWidth;
        const isTree = lines[i][pos] === '#';
        if (isTree) {
            treeCounter += 1;
        }
        index += 1;
    }
    return treeCounter;
}

const resolve = (input) => {
    const moves = [{ right: 1, down: 1 }, { right: 3, down: 1 }, { right: 5, down: 1 }, { right: 7, down: 1}, { right: 1, down: 2}]
    const [p1, p2, p3, p4, p5] = moves.map(({ right, down }) => move(input, right, down));
    return p1 * p2 * p3 * p4 * p5;
}

const testInput = parseInput(testCase);
const test = resolve(testInput);

console.log(test)
console.assert(test === 336);


